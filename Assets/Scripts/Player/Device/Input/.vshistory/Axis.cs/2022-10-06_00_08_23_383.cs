public class Axis
{
    public float x = 0f;
    public float y = 0f;
    public float jump = 0f;
    
    public void setX(float newX)
    {
        x = newX;
    }   

    public void setY(float newY)
    {
        x = newY;
    }

    public void setJump(float newJump)
    {
        x = newJump;
    }

    public void reset()
    {
        x = 0f;
        y = 0f;
        jump = 0f;
    }
}
